import UIKit

/// 系统白色17号字体
class LabelWhite17: LabelWhite {
    
    override init() {
        super.init()
        setup()
    }
    
    required init?(coder aDecoder: NSCoder) {
        super.init(coder: aDecoder)
        setup()
    }
    
    private func setup() {
        font = UIFont.systemFont(ofSize: 17)
    }
}

